public class Position {
    private int m_xPosition;
    private int m_yPosition;
    public int GetX() {
        return m_xPosition;
    }
    public int GetY() {
        return m_yPosition;
    }
    public void SetX(int x) {
        m_xPosition = x;
    }
    public void SetY(int y) {
        m_yPosition = y;
    }
}
